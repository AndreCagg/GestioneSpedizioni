    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        // TODO add your handling code here:
        JFrame frame = new JFrame();
        try {
            try {
                if (this.getT().isAlive() || this.getU().isAlive() || this.getR().isAlive() || this.getTh().isAlive()
                        || this.getSz().isAlive() || this.getMacc().isAlive() || this.getAggAut().isAlive()) {

                    frame.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
                    this.getT().join();
                    this.getU().join();
                    this.getR().join();
                    this.getTh().join();
                    this.getSz().join();
                    this.getMacc().join();
                    this.getAggAut().join();
                    frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    try {
                        conn.close();
                    } catch (SQLException sql) {
                        Err e = new Err();
                        e.setMsgtxt("DB ERROR. ERROR CODE " + sql.getErrorCode() + ", SQL STATE " + sql.getSQLState());
                        e.setVisible(true);
                    }
                    System.exit(0);
                }
            } catch (InterruptedException ie) {
                try {
                    conn.close();
                } catch (SQLException sql) {
                    Err e = new Err();
                    e.setMsgtxt("DB ERROR. ERROR CODE " + sql.getErrorCode() + ", SQL STATE " + sql.getSQLState());
                    e.setVisible(true);
                }
                ie.printStackTrace();
            }
        } catch (NullPointerException np) {
            frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        }
    }//GEN-LAST:event_formWindowClosing

